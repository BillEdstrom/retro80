// Build BOTH src/renderer/src/demon/presets.ts and music.ts from the combined
// locked captures (/tmp/demon_show_6.json, _7.json — see capture-show.mjs). The
// dance frames carry onset times and the audio carries sample offsets on the SAME
// clock, so the studio can slave the dance to the audio and land footfalls on the
// tap clicks. The loop is trimmed to the music's end.
import fs from 'node:fs'

const W = 30, H = 30
const RATE = 44100
const pack = (rows) =>
  rows.map((r) => {
    let n = 0
    for (let x = 0; x < W; x++) n = (n << 1) | (r[x] === '#' ? 1 : 0)
    return n >>> 0
  })

const META = {
  6: { id: 'show1', name: 'Preset Show #1', blurb: 'The first built-in routine — the demon\'s signature soft-shoe.' },
  7: { id: 'show2', name: 'Preset Show #2', blurb: 'The second built-in routine — a livelier number with jumps and turns.' }
}

const palette = []
const index = new Map()
const idxOf = (rows) => {
  const key = rows.join('')
  if (index.has(key)) return index.get(key)
  const i = palette.length
  index.set(key, i)
  palette.push(pack(rows))
  return i
}

const shows = []
const audios = []
for (const key of ['6', '7']) {
  const d = JSON.parse(fs.readFileSync(`/tmp/demon_show_${key}.json`, 'utf8'))
  const durSamples = d.samples[d.samples.length - 1] // loop at the music's end
  const durMs = Math.round((durSamples / RATE) * 1000)
  // dance frames within the loop
  const frames = d.frames.filter((f) => f.onsetMs < durMs)
  const seq = frames.map((f) => idxOf(f.rows))
  const onsets = frames.map((f) => f.onsetMs)
  shows.push({ ...META[key], seq, onsets, durMs })
  // audio: delta-encode the sample offsets, trimmed to durSamples
  const deltas = []
  let prev = 0
  const levels = []
  for (let i = 0; i < d.samples.length; i++) {
    if (d.samples[i] > durSamples) break
    deltas.push(d.samples[i] - prev)
    prev = d.samples[i]
    levels.push(d.levels[i])
  }
  audios.push({ id: META[key].id, durSamples, deltas, levels })
  console.log(`${META[key].id}: ${frames.length} frames, ${deltas.length} transitions, ${(durMs / 1000).toFixed(1)}s`)
}

// ---- presets.ts ----
const p = []
p.push('// AUTO-GENERATED by scripts/gen-demon-show.mjs — do not edit by hand.')
p.push('// The two preset shows of the 1979 Dancing Demon, captured frame-by-frame with')
p.push('// onset times on the same clock as the audio (so the dance locks to the music).')
p.push('')
p.push(`export const PRESET_W = ${W}`)
p.push(`export const PRESET_H = ${H}`)
p.push('')
p.push('// Shared pose palette: each entry is PRESET_H rows, each row a PRESET_W-bit int.')
p.push('export const PRESET_POSES: number[][] = [')
for (const pose of palette) p.push('  [' + pose.join(',') + '],')
p.push(']')
p.push('')
p.push('export interface PresetShow {')
p.push('  id: string')
p.push('  name: string')
p.push('  blurb: string')
p.push('  seq: number[] // pose index (into PRESET_POSES) for each frame, in order')
p.push('  onsets: number[] // ms from loop start when each frame appears (parallel to seq)')
p.push('  durMs: number // loop length (matches the audio), ms')
p.push('}')
p.push('')
p.push('export const PRESET_SHOWS: PresetShow[] = [')
for (const s of shows) {
  p.push('  {')
  p.push(`    id: ${JSON.stringify(s.id)},`)
  p.push(`    name: ${JSON.stringify(s.name)},`)
  p.push(`    blurb: ${JSON.stringify(s.blurb)},`)
  p.push(`    seq: [${s.seq.join(',')}],`)
  p.push(`    onsets: [${s.onsets.join(',')}],`)
  p.push(`    durMs: ${s.durMs}`)
  p.push('  },')
}
p.push(']')
p.push('')
fs.writeFileSync('src/renderer/src/demon/presets.ts', p.join('\n'))

// ---- music.ts ----
const m = []
m.push('// AUTO-GENERATED by scripts/gen-demon-show.mjs — do not edit by hand.')
m.push('// The exact cassette-port audio (melody + tap clicks) of each preset show, on the')
m.push('// same clock as presets.ts so the studio can lock footfalls to the tap clicks.')
m.push('')
m.push('export interface DemonAudio {')
m.push('  rate: number')
m.push('  durSamples: number')
m.push('  deltas: number[] // samples since the previous transition')
m.push('  levels: number[] // output level from this transition until the next (-1, 0, 1)')
m.push('}')
m.push('')
m.push('export const DEMON_AUDIO: Record<string, DemonAudio> = {')
for (const a of audios) {
  m.push(`  ${a.id}: {`)
  m.push(`    rate: ${RATE},`)
  m.push(`    durSamples: ${a.durSamples},`)
  m.push(`    deltas: [${a.deltas.join(',')}],`)
  m.push(`    levels: [${a.levels.join(',')}]`)
  m.push('  },')
}
m.push('}')
m.push('')
fs.writeFileSync('src/renderer/src/demon/music.ts', m.join('\n'))
console.log(`palette: ${palette.length} poses; wrote presets.ts + music.ts`)
